      const confirmMsg = document.getElementById('newsletter-confirm');
      const email      = input.value.trim();
      const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;

      if (!emailRegex.test(email)) {
        input.style.borderColor = '#ff4d4d';
        input.focus();
        return;
      }
      input.style.borderColor = '';

      fetch('https://api.brevo.com/v3/contacts', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'api-key': ''
        },
        body: JSON.stringify({ email: email, listIds: [7], updateEnabled: true })
      })
      .then(function() {
        document.querySelector('.newsletter-form-wrap').style.display = 'none';
        confirmMsg.style.display = 'block';
      })
      .catch(function(err) {
        console.error('Erreur Brevo:', err);
      });
    }

    /* Stats — compteurs animés */
    function animateCounter(el) {
      const target   = parseInt(el.getAttribute('data-target'), 10);
      if (isNaN(target)) return;
      const duration = 1800;
      let current    = 0;
      const timer = setInterval(function () {
        current += Math.ceil(target / (duration / 16));
        if (current >= target) {
          current = target;
          clearInterval(timer);
        }
        el.textContent = current;
      }, 16);
    }

    const statsSection = document.getElementById('stats-section');
    let statsAnimated  = false;
    const statsObserver = new IntersectionObserver(function (entries) {
      entries.forEach(function (entry) {
        if (entry.isIntersecting && !statsAnimated) {
          statsAnimated = true;
          document.querySelectorAll('.stat-count').forEach(function (el) {
            animateCounter(el);
          });
        }
      });
    }, { threshold: 0.3 });
    statsObserver.observe(statsSection);

  </script>

